<template>
  <div class="rb-user-avatar">
    <img v-if="avatar" :src="getImgSrc(avatar)" class="img-circle img-lg" @click="editCropper()">
    <span v-else @click="editCropper()">上传头像</span>
    <Dialog :title="title" :has-footer="false" :visible.sync="open" width="800px" append-to-body>
      <el-row>
        <el-col :xs="24" :md="12" :style="{height: '350px'}">
          <vue-cropper
            ref="cropper"
            :img="options.img ? options.img : getImgSrc(avatar) "
            :info="true"
            :auto-crop="options.autoCrop"
            :auto-crop-width="options.autoCropWidth"
            :auto-crop-height="options.autoCropHeight"
            :fixed-box="options.fixedBox"
            @realTime="realTime"
          />
        </el-col>
        <el-col :xs="24" :md="12" :style="{height: '350px'}">
          <div class="rb-avatar-upload-preview">
            <img :src="previews.url" :style="previews.img">
          </div>
        </el-col>
      </el-row>
      <br>
      <el-row>
        <el-col :lg="2" :md="2">
          <el-upload
            action="#"
            :http-request="requestUpload"
            :show-file-list="false"
            :before-upload="beforeUpload"
          >
            <el-button>
              上传
              <i class="el-icon-upload el-icon--right" />
            </el-button>
          </el-upload>
        </el-col>
        <el-col :lg="{span: 1, offset: 2}" :md="2">
          <el-button icon="el-icon-plus" @click="changeScale(1)" />
        </el-col>
        <el-col :lg="{span: 1, offset: 1}" :md="2">
          <el-button icon="el-icon-minus" @click="changeScale(-1)" />
        </el-col>
        <el-col :lg="{span: 1, offset: 1}" :md="2">
          <el-button icon="el-icon-refresh-left" @click="rotateLeft" />
        </el-col>
        <el-col :lg="{span: 1, offset: 1}" :md="2">
          <el-button icon="el-icon-refresh-right" @click="rotateRight" />
        </el-col>
        <el-col :lg="{span: 2, offset: 6}" :md="2">
          <el-button :loading="loading" type="primary" @click="uploadImg">提 交</el-button>
        </el-col>
      </el-row>
    </Dialog>
  </div>
</template>

<script>
import { VueCropper } from "vue-cropper";
import { fileUpload } from "@/api/file";
import { getImgSrc } from '@/utils';
import { mapGetters } from "vuex";

export default {
  name: "UserAvatar",
  components: { VueCropper },
  props: {
    user: {
      type: Object,
      default: () => ({}),
    },
  },
  data() {
    return {
      getImgSrc,
      // 是否显示弹出层
      open: false,
      // 弹出层标题
      title: "修改头像",
      options: {
        img: "",
        autoCrop: true, // 是否默认生成截图框
        autoCropWidth: 200, // 默认生成截图框宽度
        autoCropHeight: 200, // 默认生成截图框高度
        fixedBox: true, // 固定截图框大小 不允许改变
      },
      previews: {},
      loading: false,
    };
  },
  computed: {
    ...mapGetters([
      'avatar',
    ]),
  },
  methods: {
    // 编辑头像
    editCropper() {
      this.open = true;
    },

    // 覆盖默认的上传行为
    requestUpload() {},

    // 向左旋转
    rotateLeft() {
      this.$refs.cropper.rotateLeft();
    },

    // 向右旋转
    rotateRight() {
      this.$refs.cropper.rotateRight();
    },

    // 图片缩放
    changeScale(num) {
      num = num || 1;
      this.$refs.cropper.changeScale(num);
    },

    // 上传预处理
    beforeUpload(file) {
      if (file.type.indexOf("image/") === -1) {
        this.msgError("文件格式错误，请上传图片类型！");
      } else {
        const reader = new FileReader();
        reader.readAsDataURL(file);
        reader.onload = () => {
          this.options.img = reader.result;
        };
      }
    },

    // 上传图片
    uploadImg() {
      this.$refs.cropper.getCropBlob(data => {
        this.loading = true;

        const formData = new FormData();
        formData.append("file", data);
        formData.append("appId", 1);
        formData.append("type", 1);

        fileUpload(formData).then(response => {
          if (response.statusCode === 600) {
            this.open = false;
            this.msgSuccess("上传成功");
            this.$emit("uploadSuccess", response.data);
          } else {
            this.msgError(response.message);
          }
          this.$refs.cropper.clearCrop();
          this.loading = false;
        });
      });
    },

    // 实时预览
    realTime(data) {
      this.previews = data;
    },
  },
};
</script>

